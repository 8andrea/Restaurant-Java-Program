public class Restuarant {

    // Properties
    int waitTime;
    double rating;
    double avgPrice;
    double distance;
    String hoursOpen;

    // Constructor

    public Restuarant() {}

    public Restuarant(int waitTime, double rating, double avgPrice, double distance, String hoursOpen) {
        this.waitTime = waitTime;
        this.rating = rating;
        this.avgPrice = avgPrice;
        this.distance = distance;
        this.hoursOpen = hoursOpen;


    }

}
